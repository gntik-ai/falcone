// Embedding-provider executor (change: add-vector-search).
//
// In-platform embedding generation for KNN `queryText`. Like the functions executor,
// this is BACKEND-PLUGGABLE: it ships a deterministic local mock backend for dev/test
// and an HTTP backend for production providers (OpenAI/Cohere/etc.). A backend exposes
// `{ embed(text) -> number[] }`. Provider configuration is tenant-scoped (per workspace);
// the API key is NEVER stored in plaintext — only a `secretRef` is persisted, and the
// HTTP backend resolves the secret at request time (no caching) so key rotation is
// picked up immediately (mirrors config.secretRefs + Vault/ESO used elsewhere).
import { clientError } from './errors.mjs';

// Deterministic mock backend: produces a stable pseudo-vector of the requested dimension
// from the input text. Used by tests and local dev (no external provider call).
export function localMockEmbeddingBackend({ dimension = 8 } = {}) {
  const dim = Number(dimension);
  if (!Number.isInteger(dim) || dim < 1) {
    throw clientError('localMockEmbeddingBackend requires a positive integer dimension', 500, 'EMBEDDING_CONFIG');
  }
  return {
    dimension: dim,
    async embed(text) {
      // A simple, deterministic hash → component mapping so the same text always maps
      // to the same vector (lets cross-tenant probes pick a query nearest to a known row).
      const str = String(text ?? '');
      const out = new Array(dim);
      for (let i = 0; i < dim; i += 1) {
        let h = 2166136261 ^ (i + 1);
        for (let j = 0; j < str.length; j += 1) {
          h = Math.imul(h ^ str.charCodeAt(j), 16777619);
        }
        // Map to a bounded float in [-1, 1].
        out[i] = ((h >>> 0) % 20001) / 10000 - 1;
      }
      return out;
    }
  };
}

// HTTP backend: posts to the configured provider endpoint using the resolved secret.
// resolveSecret() is called per request (no caching). fetchImpl is injectable for tests.
export function httpEmbeddingBackend({ providerType, model, endpoint, resolveSecret, fetchImpl } = {}) {
  if (typeof resolveSecret !== 'function') {
    throw clientError('httpEmbeddingBackend requires a resolveSecret() function', 500, 'EMBEDDING_CONFIG');
  }
  const doFetch = fetchImpl ?? globalThis.fetch;
  if (typeof doFetch !== 'function') {
    throw clientError('httpEmbeddingBackend requires a fetch implementation', 500, 'EMBEDDING_CONFIG');
  }
  return {
    providerType,
    model,
    async embed(text) {
      const secret = await resolveSecret();
      if (!secret) {
        // Fail-closed: a missing Vault secret is a configuration error, never a silent
        // unauthenticated call.
        throw clientError('Embedding provider secret could not be resolved', 500, 'EMBEDDING_SECRET_UNRESOLVED');
      }
      let response;
      try {
        response = await doFetch(endpoint, {
          method: 'POST',
          headers: { 'content-type': 'application/json', Authorization: `Bearer ${secret}` },
          body: JSON.stringify({ model, input: String(text ?? '') })
        });
      } catch (error) {
        throw clientError(`Embedding provider request failed: ${error.message}`, 502, 'EMBEDDING_PROVIDER_ERROR');
      }
      if (!response.ok) {
        throw clientError(`Embedding provider returned status ${response.status}`, 502, 'EMBEDDING_PROVIDER_ERROR');
      }
      const json = await response.json();
      const embedding = json?.data?.[0]?.embedding ?? json?.embedding ?? json?.embeddings?.[0];
      if (!Array.isArray(embedding)) {
        throw clientError('Embedding provider returned no vector', 502, 'EMBEDDING_PROVIDER_ERROR');
      }
      return embedding.map((entry) => Number(entry));
    }
  };
}

// Per-workspace provider store. Persists ONLY the secretRef (never a resolved key).
// Replacing a provider returns a re-index warning (v1 does not re-embed stored data).
export function createEmbeddingProviderStore() {
  const providers = new Map(); // workspaceId -> { providerType, model, secretRef, dimension }
  return {
    async deployProvider(workspaceId, config = {}) {
      if (!workspaceId) throw clientError('Missing workspace', 400, 'WORKSPACE_MISSING');
      const existing = providers.get(workspaceId);
      // Strip any plaintext secret a caller may have (mis)passed; persist only secretRef.
      const { apiKey, secret, ...safe } = config;
      const record = {
        providerType: safe.providerType,
        model: safe.model,
        dimension: safe.dimension !== undefined ? Number(safe.dimension) : undefined,
        secretRef: safe.secretRef,
        updatedAt: new Date().toISOString()
      };
      providers.set(workspaceId, record);
      const result = { ...record };
      if (existing) {
        result.warning =
          'Embedding provider replaced. Existing vectors were generated by the previous provider/model and may require re-indexing; the platform does not re-embed stored data automatically.';
      }
      return result;
    },
    async getProvider(workspaceId) {
      return providers.get(workspaceId) ?? null;
    },
    async removeProvider(workspaceId) {
      const had = providers.delete(workspaceId);
      return { removed: had };
    }
  };
}

// Resolve a callable embedding backend for a workspace from its stored provider config.
// secretResolver(secretRef) bridges to Vault/ESO; in tests a mock backend is injected.
export function createEmbeddingExecutor(options = {}) {
  const store = options.store ?? createEmbeddingProviderStore();
  const secretResolver = options.secretResolver; // async (secretRef) -> string
  const backendFactory = options.backendFactory; // (config, resolveSecret) -> backend (test seam)

  async function resolveBackend(workspaceId) {
    const config = await store.getProvider(workspaceId);
    if (!config) {
      throw clientError('No embedding provider configured for this workspace', 422, 'EMBEDDING_PROVIDER_MISSING');
    }
    if (backendFactory) {
      return backendFactory(config, () => (secretResolver ? secretResolver(config.secretRef) : undefined));
    }
    return httpEmbeddingBackend({
      providerType: config.providerType,
      model: config.model,
      endpoint: config.endpoint ?? options.defaultEndpoint,
      resolveSecret: () => (secretResolver ? secretResolver(config.secretRef) : undefined)
    });
  }

  // Embed `text` for a workspace and (optionally) assert the result dimension matches the
  // target column dimension — a mismatch is a 422 (no DB query is attempted).
  async function embedForWorkspace(workspaceId, text, { expectedDimension } = {}) {
    const backend = await resolveBackend(workspaceId);
    const vector = await backend.embed(text);
    if (!Array.isArray(vector)) {
      throw clientError('Embedding provider returned no vector', 502, 'EMBEDDING_PROVIDER_ERROR');
    }
    if (expectedDimension !== undefined && vector.length !== Number(expectedDimension)) {
      throw clientError(
        `Embedding dimension ${vector.length} does not match the column dimension ${expectedDimension}`,
        422,
        'EMBEDDING_DIMENSION_MISMATCH'
      );
    }
    return vector;
  }

  return { store, resolveBackend, embedForWorkspace };
}
