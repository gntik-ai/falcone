// Complete the seeded fixtures for the live campaign:
//  - log in as <slug>-ops (platform realm, tenant_id claim, tenant_owner)
//  - ensure a `prod` workspace exists (seed only made `staging` due to env-name bug)
//  - provision PG (+ attempt Mongo) DB for each workspace; capture the Mongo 400 detail
//  - mint a data-plane API key per workspace via the executor (ops JWT)
//  - rewrite .fixtures.json and emit tests/live-audit/context.env (TA_*/TB_*)
import { readFileSync, writeFileSync } from 'node:fs';
const GW = process.env.FALCONE_GATEWAY || 'http://localhost:9080';
const PW = 'CampaignPass!2026';
const fx = JSON.parse(readFileSync('tests/live-campaign/.fixtures.json', 'utf8'));
const pick = (o, ...k) => { for (const x of k) if (o && o[x] != null) return o[x]; };

let cid = 0;
async function api(method, path, { token, body } = {}) {
  const h = { 'X-Correlation-Id': `cf-${Date.now()}-${++cid}`, 'X-API-Version': '2026-03-26' };
  if (token) h.Authorization = `Bearer ${token}`;
  if (body !== undefined) { h['Content-Type'] = 'application/json'; h['Idempotency-Key'] = `idem-${Date.now()}-${cid}`; }
  const r = await fetch(GW + path, { method, headers: h, body: body !== undefined ? JSON.stringify(body) : undefined });
  const ct = r.headers.get('content-type') || '';
  return { status: r.status, body: ct.includes('json') ? await r.json().catch(() => null) : await r.text() };
}
async function login(u) {
  const r = await api('POST', '/v1/auth/login-sessions', { body: { username: u, password: PW } });
  return r.body?.tokenSet?.accessToken;
}

for (const t of fx.tenants.filter(t => t.id)) {
  const tok = await login(`${t.slug}-ops`);
  t.opsUser = `${t.slug}-ops`;
  console.log(`\n=== ${t.slug} (ops token ${tok ? 'OK' : 'FAIL'}) ===`);
  // ensure prod workspace
  const haveProd = t.workspaces.some(w => w.name === 'app-prod' && w.id);
  if (!haveProd) {
    const wr = await api('POST', `/v1/tenants/${t.id}/workspaces`, { token: tok, body: { displayName: 'app-prod', slug: 'app-prod', environment: 'prod' } });
    console.log(`  create app-prod ws -> ${wr.status}`);
    const ws = { name: 'app-prod', id: pick(wr.body, 'id', 'workspaceId') || pick(wr.body?.workspace, 'id') };
    if (ws.id) t.workspaces.push(ws);
  }
  // per workspace: ensure PG db, capture mongo attempt, mint api key
  for (const ws of t.workspaces.filter(w => w.id)) {
    const pg = await api('POST', `/v1/workspaces/${ws.id}/databases`, { token: tok, body: { engine: 'postgresql' } });
    ws.pg = { status: pg.status, database: pick(pg.body, 'database') || ws.pg?.database, body: pg.body };
    const mo = await api('POST', `/v1/workspaces/${ws.id}/databases`, { token: tok, body: { engine: 'mongodb' } });
    ws.mongo = { status: mo.status, body: mo.body };
    // fetch the workspace database descriptor to learn the logical pg db name
    const gd = await api('GET', `/v1/workspaces/${ws.id}/database`, { token: tok });
    ws.dbDescriptor = gd.body;
    const ak = await api('POST', `/v1/workspaces/${ws.id}/api-keys`, { token: tok, body: { name: `${ws.name}-key`, keyType: 'service', type: 'service', scopes: ['postgres:read','postgres:write','mongo:read','mongo:write','storage:read','storage:write','events:read','events:write','functions:invoke'] } });
    ws.apiKey = { status: ak.status, key: pick(ak.body, 'apiKey', 'key', 'secret', 'token'), id: pick(ak.body, 'id', 'keyId'), body: ak.status >= 300 ? ak.body : undefined };
    console.log(`  ws ${ws.name} id=${ws.id} pg=${pg.status} mongo=${mo.status} apiKey=${ak.status}${ws.apiKey.key ? ' (minted)' : ''}`);
    if (mo.status >= 300) console.log(`    mongo body: ${JSON.stringify(mo.body).slice(0, 160)}`);
    if (ak.status >= 300) console.log(`    apikey body: ${JSON.stringify(ak.body).slice(0, 160)}`);
  }
}
writeFileSync('tests/live-campaign/.fixtures.json', JSON.stringify(fx, null, 2));

// Emit live-audit context.env (TA_*/TB_*) — pick first workspace per tenant with a db.
function envFor(t) {
  const ws = t.workspaces.find(w => w.id && (w.pg?.database || w.dbDescriptor)) || t.workspaces.find(w => w.id);
  const db = ws?.pg?.database || ws?.dbDescriptor?.database || ws?.dbDescriptor?.databaseName || 'in_falcone';
  return { tenant: t.id, ws: ws?.id, db };
}
const A = envFor(fx.tenants.find(t => t.slug === 'acme'));
const B = envFor(fx.tenants.find(t => t.slug === 'globex'));
const ctx = [
  '# Generated by complete-fixtures.mjs — live campaign fixtures for tests/live-audit/lib.sh',
  `export TA_TENANT='${A.tenant}'`, `export TA_WS='${A.ws}'`, `export TA_DB='${A.db}'`,
  `export TB_TENANT='${B.tenant}'`, `export TB_WS='${B.ws}'`, `export TB_DB='${B.db}'`,
  `export TA_OPS='acme-ops'`, `export TB_OPS='globex-ops'`, `export CAMPAIGN_PW='${PW}'`,
  '',
].join('\n');
writeFileSync('tests/live-audit/context.env', ctx);
console.log('\nwrote tests/live-campaign/.fixtures.json and tests/live-audit/context.env');
console.log(ctx);
